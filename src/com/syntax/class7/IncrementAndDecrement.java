package com.syntax.class7;

public class IncrementAndDecrement {

	public static void main(String[] args) {
		int num = 90;
		num = num + 1;
		num += 1;
		System.out.println(num);
		num++; // adding 1
		System.out.println(num);

		int num1 = 100;
		num1 -= 1;// Subtract 1
		num--;
		System.out.println(num1);
		// Increment & decrement operators can use ONLY with VAR.
		System.out.println("+++++++++++++++++++++++++++++++++++++++++++++++++++++++");
		
		
	}

}
